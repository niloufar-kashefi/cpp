#include <iostream>
#include <cmath>  // math library
#include<bits/stdc++.h> 
using namespace std;

main()
{
	cout << "sqrt is:" << sqrt(64)  << endl;
	cout << "abs is:" << abs(-64)  << endl;
	cout << "pow is:" << pow(2,10) << endl;
	
	cout << "ceil is:" << ceil(3.6) << endl;
	cout << "floor is:" << floor(-3.6) << endl;
	cout << "trunc is:" << trunc(-3.6) << endl;  //remove the decimal point
	
	cout << "log10 is:" << log10(100) << endl;
	cout << "log2 is:" << log2(32) << endl;
	
	cout << "max is:" << max(10,55);
	cout << "\nmin is:" << min(10,20);
	
return 0;
}
